function showBlogPost(articleId) {
    console.log('showBlogPost called with ID:', articleId); // Debug log
    const article = blogArticles[articleId];
    if (!article) {
        console.error('Article not found for ID:', articleId);
        return;
    }
    
    console.log('Found article:', article.title); // Debug log
    
    // Update page content
    const categoryEl = document.getElementById('post-category');
    const dateEl = document.getElementById('post-date');
    const readTimeEl = document.getElementById('post-read-time');
    const titleEl = document.getElementById('post-title');
    const contentEl = document.getElementById('post-content');
    
    if (categoryEl) categoryEl.textContent = article.category;
    if (dateEl) dateEl.textContent = article.date;
    if (readTimeEl) readTimeEl.textContent = article.readTime;
    if (titleEl) titleEl.textContent = article.title;
    if (contentEl) contentEl.innerHTML = article.content;
    
    // Update tags
    const tagsContainer = document.getElementById('post-tags');
    if (tagsContainer && article.tags) {
        tagsContainer.innerHTML = article.tags.map(tag => 
            `<span class="blog-tag">${tag}</span>`
        ).join('');
    }
    
    // Load related articles
    loadRelatedArticles(articleId);
    
    // Show blog post page
    showPage('blog-post');
    setActiveNavLink('blog');
    
    // Update sharing links
    updateSharingLinks(article);
}

function loadRelatedArticles(currentArticleId) {
    const relatedContainer = document.getElementById('related-articles');
    if (!relatedContainer) return;
    
    const currentArticle = blogArticles[currentArticleId];
    
    // Find related articles (exclude current article)
    const relatedArticles = Object.values(blogArticles)
        .filter(article => article.id !== currentArticleId)
        .slice(0, 3);
    
    relatedContainer.innerHTML = relatedArticles.map(article => `
        <div class="related-article" data-id="${article.id}">
            <div class="blog-category">${article.category}</div>
            <h4>${article.title}</h4>
            <p>${article.tags ? article.tags.join(', ') : ''}</p>
            <button class="blog-read-more">Read More</button>
        </div>
    `).join('');
}

